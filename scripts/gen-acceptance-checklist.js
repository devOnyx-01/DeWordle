#!/usr/bin/env node
/**
 * gen-acceptance-checklist.js
 *
 * Generates acceptance-checklist suggestions from changed files or a list of
 * module paths. Output is clearly marked as a suggestion and is editable.
 *
 * Usage:
 *   node scripts/gen-acceptance-checklist.js [--files <path,...>] [--base <branch>]
 *
 * Options:
 *   --files  Comma-separated list of changed file paths (overrides git diff)
 *   --base   Base branch for git diff (default: main)
 *   --json   Output raw JSON instead of Markdown
 *
 * Environment:
 *   GITHUB_BASE_REF   Used as base branch if --base not set (CI compatible)
 */

const { execSync } = require("child_process");
const path = require("path");

// ---------------------------------------------------------------------------
// Module → checklist rules
// Each entry maps a file-path pattern (regex) to checklist item templates.
// ---------------------------------------------------------------------------
const RULES = [
  {
    pattern: /^scripts\//,
    items: [
      "Script runs without errors: `node scripts/<file>`",
      "Unit test added or updated in `scripts/<basename>.test.js`",
      "Usage documented in `docs/wave/` or script header comment",
      "No hardcoded secrets or tokens",
    ],
  },
  {
    pattern: /^\.github\/workflows\//,
    items: [
      "Workflow triggers are intentional (push/pull_request/schedule)",
      "All steps have `timeout-minutes` set",
      "Secrets are referenced via `${{ secrets.NAME }}`, not hardcoded",
      "Workflow tested with `act` or a draft PR",
    ],
  },
  {
    pattern: /^docs\//,
    items: [
      "Document is reachable from an index or parent doc",
      "ADR backlinks present if architecture decision is recorded",
      "No broken relative links",
    ],
  },
  {
    pattern: /^docs\/adr\//,
    items: [
      "ADR follows the standard template (Status / Context / Decision / Consequences)",
      "Referenced from the relevant architecture doc",
      "Status is one of: Proposed | Accepted | Deprecated | Superseded",
    ],
  },
  {
    pattern: /^(backend|frontend)\//,
    items: [
      "Unit tests added or updated",
      "No new lint warnings (`npm run lint`)",
      "TypeScript types are explicit — no unchecked `any`",
      "ENV vars documented in `.env.example` if added",
    ],
  },
  {
    pattern: /^soroban\//,
    items: [
      "Contract compiles: `cargo check --manifest-path soroban/Cargo.toml`",
      "Tests pass: `cargo test --manifest-path soroban/Cargo.toml`",
      "Storage bump/migration handled if data layout changed",
    ],
  },
  {
    pattern: /^onchain\//,
    items: [
      "Cairo contract compiles with `scarb build`",
      "Tests pass with `scarb test`",
      "Interface changes reflected in `src/interfaces.cairo`",
    ],
  },
];

const DEFAULT_ITEMS = [
  "Change is covered by at least one test",
  "No unrelated files modified",
  "PR description explains the _why_, not just the _what_",
];

// ---------------------------------------------------------------------------
// Get changed files
// ---------------------------------------------------------------------------
function getChangedFiles(base) {
  try {
    const output = execSync(`git diff --name-only ${base}...HEAD`, {
      encoding: "utf8",
      stdio: ["pipe", "pipe", "pipe"],
    });
    return output.trim().split("\n").filter(Boolean);
  } catch {
    return [];
  }
}

// ---------------------------------------------------------------------------
// Build checklist
// ---------------------------------------------------------------------------
function buildChecklist(files) {
  const seen = new Set();
  const sections = [];

  for (const file of files) {
    for (const rule of RULES) {
      if (!rule.pattern.test(file)) continue;
      const key = rule.pattern.toString();
      if (seen.has(key)) continue;
      seen.add(key);

      const label = rule.pattern.toString().replace(/[/^$]/g, "").replace(/\\/g, "");
      sections.push({
        label: `${label} changes`,
        items: rule.items.map((t) =>
          t.replace("<file>", file).replace("<basename>", path.basename(file, path.extname(file)))
        ),
      });
    }
  }

  if (sections.length === 0) {
    sections.push({ label: "General", items: DEFAULT_ITEMS });
  } else {
    // Always append general items
    sections.push({ label: "General", items: DEFAULT_ITEMS });
  }

  return sections;
}

// ---------------------------------------------------------------------------
// Render
// ---------------------------------------------------------------------------
function renderMarkdown(files, sections) {
  const lines = [
    "<!-- SUGGESTION: generated by gen-acceptance-checklist.js — edit freely before submitting -->",
    "",
    "## Acceptance Checklist (suggested)",
    "",
    `> Generated from **${files.length}** changed file(s). Review and adjust before closing this issue.`,
    "",
  ];

  for (const section of sections) {
    lines.push(`### ${section.label}`);
    for (const item of section.items) {
      lines.push(`- [ ] ${item}`);
    }
    lines.push("");
  }

  lines.push("---");
  lines.push("_This checklist is a suggestion. Remove, reorder, or add items as needed._");

  return lines.join("\n");
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------
function main() {
  const args = process.argv.slice(2);
  const fileFlag = args.indexOf("--files");
  const baseFlag = args.indexOf("--base");
  const jsonMode = args.includes("--json");

  const base =
    baseFlag !== -1
      ? args[baseFlag + 1]
      : process.env.GITHUB_BASE_REF || "main";

  let files;
  if (fileFlag !== -1) {
    files = args[fileFlag + 1].split(",").map((f) => f.trim()).filter(Boolean);
  } else {
    files = getChangedFiles(base);
    if (files.length === 0) {
      process.stderr.write(`No changed files detected (diff against '${base}'). Pass --files to specify manually.\n`);
    }
  }

  const sections = buildChecklist(files);

  if (jsonMode) {
    console.log(JSON.stringify({ files, sections }, null, 2));
  } else {
    console.log(renderMarkdown(files, sections));
  }
}

main();
